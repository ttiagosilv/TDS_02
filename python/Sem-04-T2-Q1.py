valor1 = float(input("Digite o primeiro número: "))
valor2 = int(input("Digite o segundo numero "))

soma = valor1 + valor2
concatenacao = str(valor1) + str(valor2)
multiplicacao = valor1 * valor2
multiplicacao_strings = str(valor1) * int(valor2)
divisao = valor1 / valor2
divisao_inteira = valor1 // valor2 
exponenciacao = valor1 ** valor2
modulo = valor1 % valor2

print('soma:', soma)
print('concatenação:', concatenacao)
print('multiplicação:', multiplicacao) 
print('multiplicação de strings:', multiplicacao_strings)
print('divisão:', divisao)
print('divisão inteira:', divisao_inteira)
print('exponenciação:', exponenciacao) 
print('modulo:', modulo)
